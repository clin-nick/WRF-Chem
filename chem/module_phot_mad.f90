        INTEGER :: nlayer, nlevel, nn, nz
        
        REAL :: a, ak300, akt, b, c, dz, dzo, dzt, dzu, fnum, hlocal, phi1, &
          phi2, phi20, sum, tau, tdiffx, x0, xl, xl0
        REAL :: kt, q1, q2     
        INTEGER :: i, idt, ii, il, lay, lev, llbt, nr
        
        
        INTRINSIC alog, atan, exp, float, ifix, max
        
