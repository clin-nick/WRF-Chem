          qy(nj,130,nreakj), s(nj,130,nreakj), t(nn), vaer(nj), vair(nj), &
          vcld(nj), vo3(nj), vt(nj), z(nj), zmid(nj), zz(nn)
        
        REAL :: zair(nj), zt(nj)
        
        
        fnum = 0.02

        DO i = 1, nj
