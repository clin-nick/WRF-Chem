      REAL expon, expon0, expon1, divisr, temp, up, dn
      REAL ssfc
      INTEGER nlayer, mrows, lev

      INTEGER i, j



      REAL pi
      PARAMETER (pi=3.1415926535898)
      REAL largest
      PARAMETER(largest=1.E+36)
      REAL precis
      PARAMETER(precis = 1.e-7)
      REAL eps
      PARAMETER (eps = 1.E-3)













      nlevel = kz
      pifs = 1.      
      fdn0 = 0.
